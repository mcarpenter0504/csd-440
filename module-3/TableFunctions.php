/**
 * Adds two numbers together.
 *
 * @param int $number1 First random number.
 * @param int $number2 Second random number.
 *
 * @return int Sum of the two numbers.
 */
function addNumbers($number1, $number2)
{
    return $number1 + $number2;
}
